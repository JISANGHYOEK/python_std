#집합
#1. 순서가 없다.
#2. 중복 값을 허용하지 않음.
#set이란 함수는 데이터를 하나만 전달해야함으로 숫자는 [] list로 묶어서 전달해야함

a = set([1,2,3,1,1,1])
print(a)

a = set("hello world!")
print(a)

print("--------------------------------------------------------")
a = set([1,2,3,4,5,6])
b = set([4,5,6,7,8,9])

print(a & b) #교집합
print(a | b) #합집합
print(a - b) #차집합

a.add(7)
print(a)

a.update([8,9,10]) #한 번에 여러 데이터를 추가할 떄
print(a)

print("--------------------------------------------------------")
a.remove(10)
print(a)